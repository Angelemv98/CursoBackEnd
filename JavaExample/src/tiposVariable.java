public class tiposVariable {

	public static void main(String[] args) {
		double variable1 = 89.20;
		int variable2 = 23;
		int resultado = (int) variable1 + variable2; //Se puede castear directamente en la operacion
		// short es una varuable que nos soporta hasta 6 caracteres
		// byte es para numeros mas chico hasta 3
		// long es para numeros muy largos y debe terminar con L el valor para puntuarlo
		// float soporta decimales pero consume menos memoria que double y debe terminar con F el valor para puntuarlo
		// double para numeros decimales
		System.out.println(resultado);
		
		
	}

}
